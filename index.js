var disableButton = () => {
    $('.fauxq1').prop('disabled', true);
};
$(".vraiq1").click(function(){
    $(".vraiq1").css("color","green")
    $(".fauxq1").css("color", "red")
    $(document).on('click', '.vraiq1', disableButton);
    $(".vraiq1").disabled = true;
    $(".txt1").text("Bravo!")
})

